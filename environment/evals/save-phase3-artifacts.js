async function seedCompletedExperiment(projectPath, options) {
  const manifest = buildExperiment(options);
  await registerExperiment(projectPath, manifest);
  await updateExperiment(projectPath, manifest.experimentId, {
    status: 'active',
    latestAttemptId: `ATT-${manifest.experimentId}-PHASE3`,
  });
  await updateExperiment(projectPath, manifest.experimentId, {
    status: 'completed',
  });

  const artifactPath = resolveWorkspacePath(projectPath, options.outputArtifact);
  await mkdir(path.dirname(artifactPath), { recursive: true });
  await writeFile(artifactPath, options.outputContents ?? 'phase3-eval-artifact', 'utf8');

  return manifest;
}

async function packageSeedExperiment(projectPath, options) {
  const manifest = await seedCompletedExperiment(projectPath, options);
  await packageExperimentResults(projectPath, manifest.experimentId, {
    now: options.now,
    datasetHash: `sha256:${manifest.experimentId.toLowerCase()}`,
    findings: ['Phase 3 regenerated evidence remains tied to a workspace artifact.'],
    artifactMetadata: {
      [options.outputArtifact]: {
        type: options.outputType ?? 'figure',
        role: 'main-result',
        purpose: 'Support regenerated Phase 3 operator-validation evidence.',
        caption: `Regenerated evidence artifact for ${manifest.experimentId}.`,
        interpretation: 'The artifact exists only as workflow evidence, not kernel truth.',
      },
    },
    claimExportStatuses: options.claimExportStatuses ?? [],
  });
  return manifest;
}

