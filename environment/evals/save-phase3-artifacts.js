function createReader({ heads = [], unresolvedClaims = [], citations = [] } = {}) {
  const projections = {
    claimHeads: cloneValue(heads),
    unresolvedClaims: cloneValue(unresolvedClaims),
    citationChecks: cloneValue(citations),
  };

  return {
    dbAvailable: true,
    capabilities: {
      governanceProfileAtCreation: true,
      claimSearch: false,
    },
    async listClaimHeads() {
      return cloneValue(projections.claimHeads);
    },
    async listUnresolvedClaims() {
      return cloneValue(projections.unresolvedClaims);
    },
    async listCitationChecks(options = {}) {
      if (typeof options.claimId !== 'string') {
        return cloneValue(projections.citationChecks);
      }

      return cloneValue(
        projections.citationChecks.filter((entry) => entry.claimId === options.claimId),
      );
    },
  };
}

function claimReader(claimId, overrides = {}) {
  return createReader({
    heads: [{
      claimId,
      currentStatus: overrides.currentStatus ?? 'PROMOTED',
      confidence: overrides.confidence ?? 0.9,
      narrative: overrides.narrative ?? `Phase 3 evaluation claim ${claimId}.`,
      governanceProfileAtCreation: overrides.governanceProfileAtCreation ?? 'strict',
    }],
    unresolvedClaims: overrides.unresolvedClaims ?? [],
    citations: [{
      claimId,
      citationId: overrides.citationId ?? `CIT-${claimId.slice(2)}`,
      verificationStatus: overrides.verificationStatus ?? 'VERIFIED',
      retractionStatus: overrides.retractionStatus ?? 'CLEAR',
    }],
  });
}

