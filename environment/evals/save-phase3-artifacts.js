async function readAttemptRecords(projectPath, attemptId) {
  const attemptsPath = resolveInside(
    projectPath,
    '.vibe-science-environment',
    'control',
    'attempts.jsonl',
  );
  const records = await readJsonl(attemptsPath);
  return records.filter((record) => record.attemptId === attemptId);
}

async function prepareTaskWorkspace(task, projectPath) {
  if (task.taskId === 'flow-writing-advisor-pack') {
    await seedSession(projectPath);
    const reader = claimReader('C-504', { confidence: 0.91 });
    await packageSeedExperiment(projectPath, {
      experimentId: 'EXP-504',
      claimId: 'C-504',
      outputArtifact: 'plots/advisor-504.png',
      outputContents: 'advisor-figure',
      now: '2026-04-03T11:12:00Z',
    });
    await buildWritingHandoff(projectPath, {
      now: '2026-04-03T11:13:00Z',
      snapshotId: 'WEXP-2026-04-03-504',
      reader,
    });
    return { reader };
  }

  if (task.taskId === 'flow-results-export-policy') {
    const reader = claimReader('C-507', {
      confidence: 0.84,
      verificationStatus: 'PENDING',
    });
    const claimExportStatus = await exportEligibility('C-507', reader, {
      projectPath,
      requiredValidatedAfter: '2026-04-03T11:30:00Z',
    });
    const manifest = await seedCompletedExperiment(projectPath, {
      experimentId: 'EXP-507',
      claimId: 'C-507',
      outputArtifact: 'plots/results-507.png',
      outputContents: 'results-figure',
    });
    return { reader, claimExportStatus, manifest };
  }

  return {};
}

async function executeWritingHandoff(projectPath, task, options) {
  const reader = options.reader;
  return runWithMiddleware({
    projectPath,
    commandName: task.command.name,
    scope: task.command.scope,
    reader,
    commandFn: async () => {
      const payload = await buildWritingHandoff(projectPath, {
        now: options.now,
        snapshotId: options.snapshotId,
        reader,
      });
      const seed = payload.seeds[0] ?? null;
      return {
        summary: options.summary,
        warnings: payload.warnings,
        payload: {
          ...payload,
          seed,
          exportRecord: seed?.exportRecord ?? null,
        },
      };
    },
  });
}

